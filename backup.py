def get_args():
    """
    Parse command-line arguments for backup mode selection.
    """
    parser = argparse.ArgumentParser(description="Backup NFware configuration script")

    parser.add_argument(
        "--mode",
        nargs="+",
        choices=["local", "samba_ftp"],
        required=True,
        help="Choose backup mode: 'local' to save on local disk, 'samba_ftp' to save on remote FTP server, or both."
    )

    return parser.parse_args()


