def run() -> None:
    """
    Execute the complete backup workflow.
    
    Orchestrates the entire backup process:
    1. Connects to NFware server via SSH
    2. Retrieves and cleans the configuration
    3. Saves backup file locally
    4. Sends status notification
    """
    args = get_args()

    # Getting configs
    ssh_connection = None
    try:
        ssh_connection = connect_to_nfware_server_via_ssh()
        
        if not ssh_connection:
            logging.warning("SSH connection failed, aborting backup process.")
            return

        config = get_config_from_nfware_server(ssh_connection)

        if not config:
            logging.warning("No config retrieved, aborting backup process.")
            return

    except Exception as e:
        logging.error(f"Unexpected error during getting configs to backup: {e}")
        return
    finally:
        if ssh_connection:
            ssh_connection.disconnect()

    # Saving Backup configs
    try:
        if "local" in args.mode:
            save_backup_file_locally(config)
        if "samba_ftp" in args.mode:
            save_backup_file_on_samba_ftp_server(config)

        return
    except Exception as e:
        logging.error(f"Unexpected error during saving backup file: {e}")
        return


if __name__ == "__main__":
    logging.info('Starting backup process...')
    run()


